package com.example.restwithrecycle;

public class imageList {
    private String name;
    private String imageurl;

    public imageList(String name, String imageurl) {
        this.name = name;
        this.imageurl = imageurl;
    }

    public String getName() {
        return name;
    }

    public String getImageurl() {
        return imageurl;
    }
}
